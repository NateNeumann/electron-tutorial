function convertEnglish(input) {
  let str = "";
  for (char in input) {
    str += englishDict[input[char]] + " ";
  }
  return str;
}

function convertMorse(input) {
  let str = "";
  input = input.split(" ");
  for (letter in input) {
    str += morseDict[input[letter]];
  }
  return str;
}

